package info.koczwara.mundial_1;

public class Rozmiar {
    private int x;
    private int y;
    private int szer;
    private int wys;

    public Rozmiar() {
        this.x = 50;
        this.y = 20;
        this.szer = 100;
        this.wys = 50;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getSzer() {
        return szer;
    }

    public int getWys() {
        return wys;
    }

}
